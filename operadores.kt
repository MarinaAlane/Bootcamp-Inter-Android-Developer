// Pode ser chamado um comando, ao invés da expressão 

// ex : a + b === a.plus(b)
fun main() {
  var a = 5
  var b = 5
println(a.plus(b))
}

//  a - b === a.minus(b)
fun main() {
  var a = 5
  var b = 5
println(a.minus(b))
}
//  a * b === a.times(b)
fun main() {
  var a = 5
  var b = 5
println(a.times(b))
}
//  a / b === a.div(b)
fun main() {
  var a = 5
  var b = 5
println(a.div(b))
}
//  a % b === a.mod(b)
fun main() {
  var a = 5
  var b = 5
println(a.mod(b))
}

